    private void jTableResultadoMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableResultadoMousePressed
        try {
            int linha = jTableResultado.getSelectedRow();
            
            String codigo = jTableResultado.getValueAt(linha, 0).toString();
            
            ETipoAssociado tipo = new NTipoAssociado().consultar(Integer.parseInt(codigo));
            
            FrmTipoAssociadoCadastro tela = new FrmTipoAssociadoCadastro(principal, tipo);
            principal.add(tela);
            tela.setVisible(true);
            this.dispose();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, e.getMessage(), "ERRO", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jTableResultadoMousePressed


