    private void preencherTela() {
        try {
            Vector<String> cabecalho = new Vector();
            cabecalho.add("Código");
            cabecalho.add("Descrição");
            cabecalho.add("Valor da Mensalidade");
            
            Vector detalhe = new Vector();
            for(ETipoAssociado tipo : new NTipoAssociado().listar()){
                Vector<String> linha = new Vector();
                
                linha.add(tipo.getCodigo() + "");
                linha.add(tipo.getDescricao());
                linha.add(String.format(tipo.getValorMensalidade() + "", "R$ #,00"));
                detalhe.add(linha);
            }
            
            jTableResultado.setModel(new DefaultTableModel(detalhe, cabecalho));
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, e.getMessage(), "ERRO", JOptionPane.ERROR_MESSAGE);
        }   
    }
