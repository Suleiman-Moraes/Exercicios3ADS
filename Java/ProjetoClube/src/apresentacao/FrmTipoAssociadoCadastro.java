    private void jButtonSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSalvarActionPerformed
        try {
            ETipoAssociado tipoAssociado = new ETipoAssociado();
            tipoAssociado.setDescricao(jTextFieldDescricao.getText());
            tipoAssociado.setValorMensalidade(Double.parseDouble(jTextFieldMensalidade.getText()));
            
            if(!jTextFieldCodigo.getText().trim().equals("")) 
                tipoAssociado.setCodigo(Integer.parseInt(jTextFieldCodigo.getText()));
            
            new NTipoAssociado().salvar(tipoAssociado);
            
            JOptionPane.showMessageDialog(rootPane, "Dados Inseridos com sucesso!", "Informação", JOptionPane.INFORMATION_MESSAGE);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, e.getMessage(), "ERRO", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButtonSalvarActionPerformed

