    private void limparTela(){
        jTextFieldCodigo.setText("");
        jTextFieldDescricao.setText("");
        jTextFieldMensalidade.setText("");
        jTextFieldDescricao.requestFocus();
        
        jButtonDeletar.setEnabled(false);
    }
    
    private void preencherTela(ETipoAssociado tipo){
        try {
            jTextFieldCodigo.setText(tipo.getCodigo()+ "");
            jTextFieldDescricao.setText(tipo.getDescricao());
            jTextFieldMensalidade.setText(tipo.getValorMensalidade() + "");

            jButtonDeletar.setEnabled(true);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, e.getMessage(), "ERRO", JOptionPane.ERROR_MESSAGE);
        }
    }
